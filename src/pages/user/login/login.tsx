import {
  LockTwoTone,
  MailTwoTone,
  MobileTwoTone,
  // TaobaoCircleOutlined,
  UserOutlined,
  // WeiboCircleOutlined,
} from '@ant-design/icons';
import { Alert, message, Tabs } from 'antd';
import React, { useState } from 'react';
import ProForm, { ProFormCaptcha, ProFormText } from '@ant-design/pro-form';
import { useIntl, Link, history, FormattedMessage, SelectLang, useModel } from 'umi';
import Footer from '@/components/Footer';
import type { LoginParamsType } from './services/login';
import { doLogin, getSmsCaptcha } from './services/login';
import Cookies from 'js-cookie';
import { useEffect } from 'react';
import styles from './index.less';
import { generatorMenuTree, generatorMenuData, getUserMenus } from '@/utils/utils';

const LoginMessage: React.FC<{
  content: string;
}> = ({ content }) => (
  <Alert
    style={{
      marginBottom: 24,
    }}
    message={content}
    type="error"
    showIcon
  />
);

/**
 * 此方法会跳转到 redirect 参数所在的位置
 */
const goto = () => {
  if (!history) return;
  setTimeout(() => {
    // const {query} = history.location;
    // const {redirect} = query as { redirect: string };
    // history.push(redirect || '/commodity/statistics');
    history.push('/personal/info');
  }, 10);
};

// 在TS中如何定义常量/变量
// const 常量名称: 数据类型 = 值 ;
// React.FC === React.FunctionComponent === 函数式组件
const Login: React.FC = () => {
  const [submitting, setSubmitting] = useState(false);
  const [userLoginState, setUserLoginState] = useState<API.LoginStateType>({});
  const [type, setType] = useState<string>('account');
  const { initialState, setInitialState } = useModel('@@initialState');

  const intl = useIntl();
  const [form] = ProForm.useForm();

  const fetchUserInfo = async () => {
    const userInfo = await initialState?.fetchUserInfo?.();
    if (userInfo) {
      // ?????????????????????????????????????????????????????
      // 登录成功后,获取用户菜单
      let menus: API.MenuListItem[] = [];
      if (userInfo !== undefined) {
        menus = getUserMenus(userInfo);
        menus = generatorMenuTree(menus);
        // 根据获取到的菜单生成AntD Pro MenuDataItem[]
        const menuData = generatorMenuData(menus);
        setInitialState({
          ...initialState,
          currentUser: userInfo,
          menuData,
          // settings: {
          //   menu: {
          //     loading: false,
          //   },
          // },
        });
      }
      console.log(initialState!.currentUser);
      // ?????????????????????????????????????????????????????

      // setInitialState({
      //   ...initialState,
      //   currentUser: userInfo,
      // });
    }
  };

  useEffect(() => {
    const autoLogin = Cookies.get('autoLogin');
    console.log('autoLogin', autoLogin);
    if (autoLogin === 'true') {
      goto();
    }
  }, []);

  // 提交登录表单
  const handleSubmit = async (values: LoginParamsType) => {
    setSubmitting(true);
    try {
      // 登录
      const msg = await doLogin({ ...values, type });
      if (msg.status === 200) {
        message.success('登录成功！');
        await fetchUserInfo();
        console.log(msg);
        Cookies.set('autoLogin', `${values.autoLogin}`, { expires: 7 });
        goto(); // 登录成功后跳转
        return;
      }
      message.error(msg.msg, 10);

      // 如果失败去设置用户错误信息
      setUserLoginState(msg);
    } catch (error) {
      console.log(`登录失败，请重试!${error}`);
      message.error(`登录失败，请重试!${error}`);
    }
    setSubmitting(false);
  };
  const { status, type: loginType } = userLoginState;

  return (
    <div className={styles.container}>
      <div className={styles.lang}>{SelectLang && <SelectLang />}</div>
      <div className={styles.content}>
        <div className={styles.top}>
          <div className={styles.header}>
            <Link to="/">
              <img alt="logo" className={styles.logo} src={'/logo.png'} />
              <span className={styles.title}>Project Y</span>
            </Link>
          </div>
          <div className={styles.desc}>Project Y 项目描述 ......</div>
        </div>

        <div className={styles.main}>
          <ProForm
            form={form}
            initialValues={{
              autoLogin: true,
            }}
            submitter={{
              searchConfig: {
                submitText: intl.formatMessage({
                  id: 'pages.login.submit',
                  defaultMessage: '登录',
                }),
              },
              render: (_, dom) => dom.pop(),
              submitButtonProps: {
                loading: submitting,
                size: 'large',
                style: {
                  width: '100%',
                },
              },
            }}
            onFinish={async (values) => {
              await handleSubmit(values as LoginParamsType);
            }}
          >
            <Tabs activeKey={type} onChange={setType}>
              <Tabs.TabPane
                key="account"
                tab={intl.formatMessage({
                  id: 'pages.login.accountLogin.tab',
                  defaultMessage: '账户密码登录',
                })}
              />
              <Tabs.TabPane
                key="mobile"
                tab={intl.formatMessage({
                  id: 'pages.login.phoneLogin.tab',
                  defaultMessage: '手机号登录',
                })}
              />
            </Tabs>

            {status === 'error' && loginType === 'account' && (
              <LoginMessage
                content={intl.formatMessage({
                  id: 'pages.login.accountLogin.errorMessage',
                  defaultMessage: '账户或密码错误（admin/ant.design)',
                })}
              />
            )}
            {type === 'account' && (
              <>
                <ProFormText
                  name="username"
                  fieldProps={{
                    size: 'large',
                    prefix: <UserOutlined className={styles.prefixIcon} />,
                  }}
                  placeholder={intl.formatMessage({
                    id: 'pages.login.username.placeholder',
                    defaultMessage: '用户名: admin or user',
                  })}
                  rules={[
                    {
                      required: true,
                      message: (
                        <FormattedMessage
                          id="pages.login.username.required"
                          defaultMessage="请输入用户名!"
                        />
                      ),
                    },
                  ]}
                />
                <ProFormText.Password
                  name="password"
                  fieldProps={{
                    size: 'large',
                    prefix: <LockTwoTone className={styles.prefixIcon} />,
                  }}
                  placeholder={intl.formatMessage({
                    id: 'pages.login.password.placeholder',
                    defaultMessage: '密码: ant.design',
                  })}
                  rules={[
                    {
                      required: true,
                      message: (
                        <FormattedMessage
                          id="pages.login.password.required"
                          defaultMessage="请输入密码！"
                        />
                      ),
                    },
                  ]}
                />
              </>
            )}

            {status === 'error' && loginType === 'mobile' && <LoginMessage content="验证码错误" />}
            {type === 'mobile' && (
              <>
                <ProFormText
                  fieldProps={{
                    size: 'large',
                    prefix: <MobileTwoTone className={styles.prefixIcon} />,
                  }}
                  name="mobilePhone"
                  placeholder={intl.formatMessage({
                    id: 'pages.login.phoneNumber.placeholder',
                    defaultMessage: '手机号',
                  })}
                  rules={[
                    {
                      required: true,
                      message: (
                        <FormattedMessage
                          id="pages.login.phoneNumber.required"
                          defaultMessage="请输入手机号！"
                        />
                      ),
                    },
                    {
                      pattern: /^\d{10}$/,
                      message: (
                        <FormattedMessage
                          id="pages.login.phoneNumber.invalid"
                          defaultMessage="手机号格式错误！"
                        />
                      ),
                    },
                  ]}
                />
                <ProFormCaptcha
                  fieldProps={{
                    size: 'large',
                    prefix: <MailTwoTone className={styles.prefixIcon} />,
                  }}
                  captchaProps={{
                    size: 'large',
                  }}
                  placeholder={intl.formatMessage({
                    id: 'pages.login.captcha.placeholder',
                    defaultMessage: '请输入验证码',
                  })}
                  captchaTextRender={(timing, count) => {
                    if (timing) {
                      return `${count} ${intl.formatMessage({
                        id: 'pages.getCaptchaSecondText',
                        defaultMessage: '获取验证码',
                      })}`;
                    }
                    return intl.formatMessage({
                      id: 'pages.login.phoneLogin.getVerificationCode',
                      defaultMessage: '获取验证码',
                    });
                  }}
                  name="smsCaptcha"
                  rules={[
                    {
                      required: true,
                      message: (
                        <FormattedMessage
                          id="pages.login.captcha.required"
                          defaultMessage="请输入验证码！"
                        />
                      ),
                    },
                  ]}
                  onGetCaptcha={async (mobilePhone) => {
                    console.log('mobilePhone', mobilePhone);
                    const tempMobilePhone = form.getFieldValue('mobilePhone');
                    if (!tempMobilePhone) {
                      message.error('请输入手机号码!', 10);
                      return;
                    }
                    const m = form.getFieldsError(['mobilePhone']);
                    if (m[0].errors.length > 0) {
                      message.error('手机号码格式有错误!', 10);
                      return;
                    }
                    const result = await getSmsCaptcha(tempMobilePhone);
                    console.log(result);
                    if (result.status !== 201) {
                      message.error('获取验证码失败!!!!!!!');
                      message.error(result.msg);
                      return;
                    }
                    message.success('获取验证码成功!');
                  }}
                />
              </>
            )}
            <div
              style={{
                marginBottom: 24,
              }}
            >
              {/* <ProFormCheckbox noStyle name="autoLogin"> */}
              {/*  <FormattedMessage id="pages.login.rememberMe" defaultMessage="自动登录"/> */}
              {/* </ProFormCheckbox> */}
              {/* <a */}
              {/*  style={{ */}
              {/*    float: 'right', */}
              {/*  }} */}
              {/* > */}
              {/*  <FormattedMessage id="pages.login.forgotPassword" defaultMessage="忘记密码"/> */}
              {/* </a> */}
            </div>
          </ProForm>
          {/* <Space className={styles.other}> */}
          {/*  <FormattedMessage id="pages.login.loginWith" defaultMessage="其他登录方式"/> */}
          {/*  /!* <TaobaoCircleOutlined className={styles.icon}/> *!/ */}
          {/*  /!* <WeiboCircleOutlined className={styles.icon}/> *!/ */}
          {/*  <GoogleOutlined className={styles.icon}/> */}
          {/*  <GithubOutlined className={styles.icon}/> */}
          {/*  <AlipayCircleOutlined className={styles.icon}/> */}
          {/* </Space> */}
        </div>
      </div>
      <Footer />
    </div>
  );
};
export default Login;
